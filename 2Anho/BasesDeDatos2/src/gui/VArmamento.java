    public void buscarArmamento(){
        ModeloTablaArmamento m;
    
        m=(ModeloTablaArmamento) armamentoTable.getModel();
        m.setFilas(fa.obtenerArmamento(aSerieBuscarText.getText(), aModeloBuscarText.getText(), aTipoBuscarText.getText()));
        //Funcion Principal -> FachApp -> GestionUsuarios -> FachBD ->  DAO Usuarios
        
        if (m.getRowCount() > 0) {
            armamentoTable.setRowSelectionInterval(0, 0); //Cojemos la primera fila de la tabla
            aSerieIntText.setText( String.valueOf(m.obtenerArma(armamentoTable.getSelectedRow()).getNumSerie()) );
            aModeloIntText.setText(m.obtenerArma(armamentoTable.getSelectedRow()).getModelo());
            aTipoIntText.setText(m.obtenerArma(armamentoTable.getSelectedRow()).getTipo());
            aEstadoIntText.setText(m.obtenerArma(armamentoTable.getSelectedRow()).getEstado());
            aUbicacionIntText.setText(m.obtenerArma(armamentoTable.getSelectedRow()).getUbicacion());
            aFechaIntText.setText( String.valueOf(m.obtenerArma(armamentoTable.getSelectedRow()).getFechaAdquisicion()) );

            aGuardarButton.setEnabled(true);
            aBorrarButton.setEnabled(true);
        }
        else{
            aGuardarButton.setEnabled(false);
            aBorrarButton.setEnabled(false);
        }
    }
    
