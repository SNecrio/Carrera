    private void eGuardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eGuardarButtonActionPerformed

        ModeloTablaEquipo m;
        m = (ModeloTablaEquipo)equipoTable.getModel();

        int id = Integer.parseInt(eIdIntText.getText());
        String companhia = eCompanhiaIntText.getText();
        String nombre = eNombreIntText.getText();
        String tipo = eTipoIntText.getText();
        Date fechaAdquisicion;
        String propiedad = eModoIntText.getText();

        try{
            fechaAdquisicion = formato.parse(eFechaIntText.getText());
        }catch(ParseException e){
            fechaAdquisicion = new GregorianCalendar(2000, Calendar.JANUARY, 1).getTime();
        }

        Equipo nuevoEquipo = new Equipo(id, companhia, nombre,tipo,fechaAdquisicion,idGrupo,propiedad);

        if(eModoNuevo){
            fa.insertarEquipo(nuevoEquipo, modoEquipo);

            eNombreIntText.setText("");
            eTipoIntText.setText("");
            eFechaIntText.setText("");
            eModoIntText.setText("");
            eCompanhiaIntText.setText("");
            eIdIntText.setText("");

            eNuevoAvisoLabel.setText(" ");
            eModoNuevo = false;

        }else{
            Equipo selectedEquipo = m.obtenerEquipo(equipoTable.getSelectedRow());
            fa.actualizarEquipo(selectedEquipo.getId(), nuevoEquipo, modoEquipo);
        }

        m.setFilas(fa.obtenerEquipo(eIdBuscarText.getText(), eNombreBuscarText.getText(), modoEquipo));

    }//GEN-LAST:event_eGuardarButtonActionPerformed

