    public void buscarCuarteles(){
        ModeloTablaCuartelAcceder m;
        
        int idGrupo = miembro.getIdGrupo();
        String nombreCuartel = buscarNombre.getText();
        
        m = (ModeloTablaCuartelAcceder) TablaCuartelMenu.getModel();
        m.setFilas(fa.obtenerCuarteles(nombreCuartel, idGrupo));
        
        if(m.getRowCount() > 0){
            TablaCuartelMenu.setRowSelectionInterval(0, 0);
        }
    }
