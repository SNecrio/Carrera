    private void TablaCuartelMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaCuartelMenuMouseClicked
        // TODO add your handling code here:
        
        ModeloTablaCuartelAcceder m = (ModeloTablaCuartelAcceder) TablaCuartelMenu.getModel();
        int fila = TablaCuartelMenu.getSelectedRow();
        
        if(fila != -1){
            Cuartel cuartel = m.obtenerCuarteles(TablaCuartelMenu.getSelectedRow());
            
            if(fa.existeMiembroCuartel(miembro.getDni(), cuartel.getNombre())){
                BotonAsignar.setText("Deasignar");
            }else{
                BotonAsignar.setText("Asignar");
            }
        }
    }//GEN-LAST:event_TablaCuartelMenuMouseClicked

    private void BotonAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAsignarActionPerformed

        ModeloTablaCuartelAcceder m = (ModeloTablaCuartelAcceder) TablaCuartelMenu.getModel();
        Cuartel cuartel = m.obtenerCuarteles(TablaCuartelMenu.getSelectedRow());
        if(fa.existeMiembroCuartel(miembro.getDni(), cuartel.getNombre())){
            fa.borrarAsignacion(miembro.getDni(), cuartel.getNombre());
            BotonAsignar.setText("Asignar");
        }else{
            fa.asignarMiembroCuartel(miembro,cuartel);
            BotonAsignar.setText("Deasginar");
        }
        
    }//GEN-LAST:event_BotonAsignarActionPerformed


